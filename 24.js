// break and continue keyword 


// 1.Break keyword 

for (let i = 1; i <= 10; i++) {
    if (i === 4) {
        break;
    }
    console.log(i);
}


console.log("the break line ");

// 2.Continue keyword

for (let i = 1; i <= 6; i++) {
    if (i === 4) {
        continue;
    }
    console.log(i);
}
console.log("there are fine");